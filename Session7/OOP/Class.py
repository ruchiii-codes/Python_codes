# Class is a blueprint for creating objects.
# All datatypes in python are classes.
# Class is a set of rules which follows by the objects created from that class.
# Class Type : 1. Built in Class   2. User Defined Class
# Class contains Data/Properties and Functions/Behaviour. 
# EX : Car class -> Data/properties: color, model, price   Functions/Behaviour: start, stop, accelerate

L = [1,2,3]
print(type(L)) # <class 'list'>
L.upper() # AttributeError: 'list' object has no attribute 'upper'

