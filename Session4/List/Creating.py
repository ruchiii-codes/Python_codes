#Empty 
print([])

# 1D -> Homogeneous List
print([1, 2, 3, 4, 5])

# 2D -> Heterogeneous (Nested List)
print([1, 2, 3,[4, 5]])

# 3D -> Homogeneous
print([[[1, 2], [3, 4]], [[5, 6], [7, 8]]])

#Heterogeneous List
print([1, 'Hello', 3.14, [1, 2, 3], True, (5+6j)])

#Using Type Conversion
print(list('Hello'))  # Converts string to list of characters